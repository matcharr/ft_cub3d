#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int carre;

	carre = 0;
	i = 1;
	while (i <= nb / 2)
	{
		carre = i * i;
		if (carre == nb)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
